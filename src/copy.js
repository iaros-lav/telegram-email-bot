export function createCopy({ privacyUrl = "", newsletterScheduleText = "Следующее письмо обычно приходит по пятницам." } = {}) {
  return {
    privacyUrl,
    newsletterScheduleText,
    startLines(hasMiniApp) {
      const lines = [
        "Здесь можно подписаться на новости Немного Нервно,",
        "чтобы не потерять друг друга на этой большой и чужой Земле просто отправьте ваш email и страну.",
        "",
        "Это нужно, чтобы делиться важными постами, объявлениями и редкими письмами рассылки. Страна нужна для сегментации аудитории и более понятной аналитики, но её можно не указывать.",
      ];

      if (hasMiniApp) {
        lines.push("", "Если удобнее, нажмите кнопку ниже и заполните красивую форму.");
      }

      lines.push(
        "Удалить свои данные можно командой /delete."
      );

      if (privacyUrl) {
        lines.push(`Политика конфиденциальности: ${privacyUrl}`);
      }

      return lines;
    },
    miniApp: {
      pageTitle: "Подписка по email",
      title: "Оставайтесь на связи",
      description: "Оставьте email и страну, чтобы не потерять друг друга на этой большой и чужой Земле. Позже вы сможете изменить данные, снова открыв эту форму или отправив боту /start.",
      emailLabel: "Email",
      emailPlaceholder: "name@example.com",
      countryLabel: "Страна",
      countryPlaceholder: "Например, Russia",
      consentLabel: "Я согласен(на), что владелец канала может хранить мой email и страну для новостей и связи.",
      submitButton: "Сохранить данные",
      note: "Отправляя форму, вы соглашаетесь, что владелец канала может хранить ваш email и страну для связи и рассылки новостей.",
      successTitle: "Готово, вы в списке.",
      successDescription: `${newsletterScheduleText} Окно можно закрыть. Если захотите удалить данные позже, отправьте боту /delete.`,
      openFromTelegramError: "Эту Mini App нужно открывать прямо из Telegram, чтобы бот понял, кто отправляет форму.",
      genericSaveError: "Не удалось сохранить данные.",
      closeButton: "Закрыть",
      consentRequiredError: "Нужно подтвердить согласие на хранение данных перед отправкой формы."
    },
    privacy: {
      title: "Конфиденциальность",
      summaryLines: [
        "Я храню ваш Telegram ID, базовые поля профиля, источник подписки, текущий email и указанную страну.",
        "Эти данные используются для управления email-подписками канала.",
        "Включён EmailOctopus, ваш email также отправляется туда для доставки писем.",
        "Удалить локальные данные можно в любой момент командой /delete."
      ],
      pageIntro: "Этот бот сохраняет Telegram ID, базовые публичные поля профиля, источник подписки, один текущий email и указанную страну, чтобы владелец канала мог управлять подписками на рассылку.",
      pageEmailOctopus: "Включён EmailOctopus, отправленный email также передаётся туда для доставки писем.",
      pageBullets: [
        "Email используется для новостей канала, объявлений и редких писем рассылки.",
        "Страна нужна для сегментации аудитории и более понятной аналитики.",
        "Локальные данные можно удалить в любой момент командой /delete.",
        "Изменить email или страну можно в любой момент, отправив /start заново."
      ]
    },
    countryPrompt(currentCountry = "") {
      const lines = [
        "Теперь укажите вашу страну.",
        "Пожалуйста, пишите только на английском.",
        "Например: Russia, Ukraine, Kazakhstan, Germany."
      ];

      if (currentCountry) {
        lines.push(`Сейчас у вас сохранена страна: ${currentCountry}.`);
      }

      lines.push("Если не хотите указывать страну или хотите оставить текущую, отправьте /skip.");
      return lines;
    },
    signupSuccess(country = "") {
      if (country) {
        return `Готово.: ${country}. ${newsletterScheduleText} Если захотите обновить данные позже, отправьте /start.`;
      }

      return `Готово. ${newsletterScheduleText} Если захотите позже добавить или изменить страну, отправьте /start.`;
    }
  };
}
